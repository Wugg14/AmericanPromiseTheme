<h2>Why do we need an Amendment to the U.S. Constitution?</h2>
<i><p>We need a constitutional amendment because we have a constitutional problem. A series of narrow decisions over the past 40 plus years has locked the Supreme Court into a doctrine that effectively denies the principle of political equality.
  </p><p>While individual reforms—such as disclosure, ranked-choice voting, and open primaries—are crucial, until we clarify the foundational principle of political equality through the American Promise Amendment, our system of representative government will continue to be at risk. Our Amendment provides that foundation, by establishing the government’s compelling interest in political equality, authorizing our Congress and the states to set common sense limits on political spending and distinguish between legal entities and natural persons. Without an amendment, Congress and the states are prohibited from setting limits on outside political spending and other reforms are at risk of being swept away by a change in the political winds.
  </p><p>
  to learn more, please see <a href="https://www.americanpromise.net/who-we-are/faq/">the American Promise FAQ</a>
</p></i>
<div class="anchor">

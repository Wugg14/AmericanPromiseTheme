<h2>What is the solution to level the playing field in America’s Marketplace?</h2>
<i><p>The current dysfunction of our political system is not self-correcting; systemic change is required to restore the health of our civic institutions and the integrity of our government.
</p><p>Amazingly, 75% of Americans agree on the solution to pay-to-play politics; they are convinced that we need a 28th Constitutional Amendment to end unlimited political spending, distinguish between legal entities and natural persons, and affirm our national interest in political equality. Thus far, twenty states have passed resolutions calling for the 28th Amendment as one of several critical reforms needed to secure our republic.
</p><p>
To learn more, please see <a href="https://www.americanpromise.net/take-action/join-a-program/business-for-american-promise/pay-to-play-is-a-losing-proposition-for-business-society/">Why Pay-to-Play Politics is a Losing Proposition for Business and Society.</a>
</p></i>
<div class="anchor">

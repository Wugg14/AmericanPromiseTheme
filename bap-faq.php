<h2>What’s the process for passing a Constitutional Amendment?</h2>
<i><p>The Constitution provides that an amendment may be proposed either by the Congress with a two-thirds majority vote in both the House of Representatives and the Senate or by a constitutional convention called by Congress upon request by two-thirds of the state legislatures. Once an amendment is passed, it must be ratified by three-fourths of the states. Though it is reasonable to wonder if Congress and the states can manage to do this, all 27 amendments of our Constitution have passed by 2/3 of Congress and been ratified by 3/4 of the states. It’s what Americans do: <a href="https://www.youtube.com/watch?v=K6igolnTvqA&feature=youtu.be">watch this.</a>
</p></i>

